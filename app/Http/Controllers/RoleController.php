        $data = $request->all();
            
        $rules = array(
           'rolename' => 'required' ,
        );

        $validate=Validator::make($data,$rules);
        
        if ($validate->fails()) {
            return redirect()->back()->withInput()->withErrors($validate);
        }
        else{
           
            $form_data = array(
               'name' => $data['rolename'],
            );
        //    dd($form_data);
           $Role = role::create($form_data);
           
           return redirect('/Admin/AssignRole')->with('success');
        }   
    }

